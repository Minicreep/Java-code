
public class Loops {
	public static void main(String[] args) {
		
		System.out.println("for loop:");
		
		for(int i=1;i<=10;i++) {
			System.out.println(i+" ");
		}
		
		System.out.println("while loop:");
		int j=1;
		while(j<=10) {
			System.out.println(j+" ");
			j++;
		}
		
		System.out.println("do-while loop:");
		int k=1;
		do {
			System.out.println(k+" ");
			k++;
		}while(k<=10);
		
				
	}

}
